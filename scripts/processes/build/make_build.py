from data.paths import JoinPaths
from data.settings import Settings
from data.common import SetupTemplate
from processes.build.abstract_build_system import BuildSpec, BuildSystem


# ---------------------------------------------------------------------------
# Make implementation
# ---------------------------------------------------------------------------

MAKE_SPEC_FILENAME = "pb_build_spec.mk"


class MakeBuildSystem(BuildSystem):
    NAME = "make"

    def generate_repo_spec(self, repository) -> str:
        spec = BuildSpec.from_repo(repository)
        if spec is None or spec.is_empty():
            return ""

        repo_name = repository.get("repo name", repository.get("name", ""))
        repo_src  = repository.get("repo source", "")

        exe_names   = [exe["name"] for exe in spec.executables]
        test_names  = [t["name"] for t in spec.tests]

        if len(exe_names) != len(set(exe_names)):
            dupes = sorted(set(n for n in exe_names if exe_names.count(n) > 1))
            raise ValueError(f"Duplicate executable names in repo '{repo_name}': {dupes}")
        if len(test_names) != len(set(test_names)):
            dupes = sorted(set(n for n in test_names if test_names.count(n) > 1))
            raise ValueError(f"Duplicate test names in repo '{repo_name}': {dupes}")

        lines = [
            '# PROJECTBASE (auto-generated from configs.json "build" section)',
            "# DO NOT EDIT — regenerated by ProjectBase on every setup",
            "",
        ]

        if spec.sources:
            srcs = " ".join(f"{repo_src}/{s}" for s in spec.sources)
            lines.append(f"REPO_SOURCES := {srcs}")

        if spec.executables:
            lines.append("REPO_EXECS := " + " ".join(e["name"] for e in spec.executables))
            for exe in spec.executables:
                lines.append(f'EXEC_{exe["name"]}_SRC := {repo_src}/{exe["source"]}')
                lines.append(f'EXEC_{exe["name"]}_FLAGS := {" ".join(exe.get("cflags", []))}')

        if spec.tests:
            lines.append("REPO_TESTS := " + " ".join(t["name"] for t in spec.tests))
            for test in spec.tests:
                lines.append(f'TEST_{test["name"]}_SRC := {repo_src}/{test["source"]}')
                lines.append(f'TEST_{test["name"]}_FLAGS := {" ".join(test.get("cflags", []))}')

        if spec.cflags:
            lines.append(f'REP_CFLAGS := {" ".join(spec.cflags)}')
        if spec.lib_cflags:
            lines.append(f'REP_LIB_CFLAGS := {" ".join(spec.lib_cflags)}')
        if spec.exe_cflags:
            lines.append(f'REP_EXE_CFLAGS := {" ".join(spec.exe_cflags)}')
        if spec.test_cflags:
            lines.append(f'REP_TEST_CFLAGS := {" ".join(spec.test_cflags)}')

        return "\n".join(lines) + "\n"

    def generate_project_flags(self, project_spec: "ProjectBuildSpec | None") -> str:
        if project_spec is None or project_spec.is_empty():
            return ""
        lines = ['# Project-wide compiler flags (from root repository configs.json "project" section)']
        if project_spec.cflags:
            lines.append(f'CFLAGS += {" ".join(project_spec.cflags)}')
        if project_spec.debug_cflags:
            lines.append("ifeq ($(BUILD_TYPE),Debug)")
            lines.append(f'CFLAGS += {" ".join(project_spec.debug_cflags)}')
            lines.append("endif")
        if project_spec.release_cflags:
            lines.append("ifeq ($(BUILD_TYPE),Release)")
            lines.append(f'CFLAGS += {" ".join(project_spec.release_cflags)}')
            lines.append("endif")
        lines.append("")
        return "\n".join(lines)

    def setup_project(self, repos_include_entries: list, project_vars: dict,
                      project_spec: "ProjectBuildSpec | None" = None):
        SetupTemplate(
            "project/Makefile",
            JoinPaths(Settings["paths"]["build env"], "Makefile"),
            {
                "INCLUDE_REPOSITORY_MAKEFILES": " ".join(repos_include_entries),
                "PROJECT_CFLAGS": self.generate_project_flags(project_spec),
            }
            | project_vars,
        )

    def get_build_command(self) -> str:
        from processes.process import GetEnvVarExports
        cmd  = f"{GetEnvVarExports()}; "
        cmd += f"make -f {Settings['paths']['build env']}/Makefile -j $(nproc) -k"
        return cmd

    def get_test_groups(self, repository) -> dict:
        spec = BuildSpec.from_repo(repository)
        if spec is None:
            return {}
        repo_name = repository.get("repo name", repository.get("name", ""))
        groups = {}
        for test in spec.tests:
            grp = test.get("group")
            if grp:
                groups[f"{repo_name}_{test['name']}"] = grp
        return groups

    def get_repo_build_filename(self) -> str:
        return "Makefile"

    def get_repo_template(self) -> str:
        return "repository/Makefile"

    def get_spec_filename(self) -> "str | None":
        return MAKE_SPEC_FILENAME

    def make_repo_include_entry(self, repo_source: str, build_path: str,
                                is_independent: bool) -> str:
        return JoinPaths(build_path, "Makefile")

