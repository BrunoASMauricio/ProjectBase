import os
from data.paths import JoinPaths
from data.settings import Settings
from data.common import SetupTemplate
from processes.build.abstract_build_system import BuildSpec, BuildSystem

# ---------------------------------------------------------------------------
# CMake implementation
# ---------------------------------------------------------------------------

_DATA_TYPE_TO_MACRO = {
    "test_data":       "SETUP_TEST_DATA",
    "test_exec":       "SETUP_TEST_EXEC",
    "executable_data": "SETUP_EXECUTABLE_DATA",
    "executable_exec": "SETUP_EXECUTABLE_EXEC",
}

# Name of the auto-generated per-repo cmake spec (written to the build path)
CMAKE_SPEC_FILENAME = "pb_build_spec.cmake"


class CMakeBuildSystem(BuildSystem):
    NAME = "cmake"

    def has_manual_cmake(self, repository) -> bool:
        """True when the repo ships its own configs/CMakeLists.txt."""
        return os.path.isfile(
            JoinPaths(repository["repo source"], "configs", "CMakeLists.txt")
        )

    def generate_repo_spec(self, repository) -> str:
        """
        Generate cmake content from the "build" JSON section — equivalent to
        what the developer would write in their configs/CMakeLists.txt.
        Returns "" when no "build" section is present or it is empty.
        """
        spec = BuildSpec.from_repo(repository)
        if spec is None or spec.is_empty():
            return ""

        repo_name = repository.get("repo name", repository.get("name", ""))
        lib_target = f"{repo_name}_lib"

        # Collect cmake target names
        exe_names = [exe["name"] for exe in spec.executables]
        test_targets = [f"{repo_name}_{t['name']}" for t in spec.tests]

        # Duplicate executable names?
        if len(exe_names) != len(set(exe_names)):
            dupes = sorted(set(n for n in exe_names if exe_names.count(n) > 1))
            raise ValueError(
                f"Duplicate executable names in repo '{repo_name}': {dupes}"
            )

        # Duplicate test names?
        if len(test_targets) != len(set(test_targets)):
            dupes = sorted(set(n for n in test_targets if test_targets.count(n) > 1))
            raise ValueError(
                f"Duplicate test names in repo '{repo_name}': {dupes}"
            )

        # Executable name conflicts with test cmake target?
        conflicts = set(exe_names) & set(test_targets)
        if conflicts:
            raise ValueError(
                f"Executable name(s) conflict with test cmake targets in '{repo_name}': {sorted(conflicts)}"
            )

        # Any target conflicts with the library target?
        all_targets = set(exe_names) | set(test_targets)
        if lib_target in all_targets:
            raise ValueError(
                f"Target name '{lib_target}' conflicts with the auto-generated library target in repo '{repo_name}'"
            )

        lines = [
            "# PROJECTBASE (auto-generated from configs.json \"build\" section)",
            "# DO NOT EDIT — regenerated by ProjectBase on every setup",
            "",
        ]

        # Source files → shared library
        if spec.sources:
            lines.append("SET(repository_source_files")
            for src in spec.sources:
                lines.append(f'    "${{REPO_SRC_PATH}}/{src}"')
            lines.append(")\n")

        # Executables
        for exe in spec.executables:
            lines.append(
                f'COMPILE_EXECUTABLE({exe["name"]} ${{REPO_SRC_PATH}}/{exe["source"]})'
            )
            if exe.get("cflags"):
                flags = " ".join(exe["cflags"])
                lines.append(f'target_compile_options({exe["name"]} PRIVATE {flags})')
        if spec.executables:
            lines.append("")

        # Tests  (cmake target name is ${REPO_NAME}_<test_name> per COMPILE_TEST macro)
        for test in spec.tests:
            lines.append(
                f'COMPILE_TEST({test["name"]} ${{REPO_SRC_PATH}}/{test["source"]})'
            )
            if test.get("cflags"):
                flags = " ".join(test["cflags"])
                lines.append(
                    f'target_compile_options(${{REPO_NAME}}_{test["name"]} PRIVATE {flags})'
                )
        if spec.tests:
            lines.append("")

        # Data / exec setup macros
        for item in spec.data:
            macro = _DATA_TYPE_TO_MACRO.get(item["type"], "SETUP_TEST_DATA")
            lines.append(f'{macro}(${{REPO_SRC_PATH}}/{item["path"]})')
        if spec.data:
            lines.append("")

        # Compiler flags applied to all targets
        if spec.cflags:
            lines.append(f'SET(REP_C_FLAGS {" ".join(spec.cflags)})')
        if spec.debug_cflags:
            lines.append(f'SET(REP_DEBUG_C_FLAGS {" ".join(spec.debug_cflags)})')
        if spec.release_cflags:
            lines.append(f'SET(REP_RELEASE_C_FLAGS {" ".join(spec.release_cflags)})')
        # Target-specific additional flags
        if spec.lib_cflags:
            lines.append(f'SET(REP_LIB_C_FLAGS {" ".join(spec.lib_cflags)})')
        if spec.exe_cflags:
            lines.append(f'SET(REP_EXE_C_FLAGS {" ".join(spec.exe_cflags)})')
        if spec.test_cflags:
            lines.append(f'SET(REP_TEST_C_FLAGS {" ".join(spec.test_cflags)})')

        return "\n".join(lines) + "\n"

    def generate_project_flags(self, project_spec: "ProjectBuildSpec | None") -> str:
        """
        Return cmake add_compile_options() statements for project-wide flags.
        Debug/Release flags use generator expressions so a single cmake
        configure covers both build types.
        """
        if project_spec is None or project_spec.is_empty():
            return ""

        lines = [
            "# Project-wide compiler flags (from root repository configs.json \"project\" section)",
        ]
        if project_spec.cflags:
            lines.append(
                "add_compile_options(" + " ".join(project_spec.cflags) + ")"
            )
        if project_spec.debug_cflags:
            gen_exprs = " ".join(
                f"$<$<CONFIG:Debug>:{f}>" for f in project_spec.debug_cflags
            )
            lines.append(f"add_compile_options({gen_exprs})")
        if project_spec.release_cflags:
            gen_exprs = " ".join(
                f"$<$<CONFIG:Release>:{f}>" for f in project_spec.release_cflags
            )
            lines.append(f"add_compile_options({gen_exprs})")
        lines.append("")
        return "\n".join(lines)

    def setup_project(self, repos_include_entries: list, project_vars: dict,
                      project_spec: "ProjectBuildSpec | None" = None):
        SetupTemplate(
            "project/CMakeLists.txt",
            JoinPaths(Settings["paths"]["build env"], "CMakeLists.txt"),
            {
                "INCLUDE_REPOSITORY_CMAKELISTS": "\n".join(repos_include_entries),
                "PROJECT_CFLAGS": self.generate_project_flags(project_spec),
            }
            | project_vars,
        )

    def get_test_groups(self, repository) -> dict:
        spec = BuildSpec.from_repo(repository)
        if spec is None:
            return {}
        repo_name = repository.get("repo name", repository.get("name", ""))
        groups = {}
        for test in spec.tests:
            grp = test.get("group")
            if grp:
                groups[f"{repo_name}_{test['name']}"] = grp
        return groups

    def get_build_command(self) -> str:
        from processes.process import GetEnvVarExports
        cmd  = f"{GetEnvVarExports()}; "
        cmd += "cmake -DCMAKE_BUILD_TYPE=Debug"
        cmd += " --no-warn-unused-cli"
        cmd += " -DCMAKE_EXPORT_COMPILE_COMMANDS=1"
        cmd += f" -S {Settings['paths']['build env']}"
        cmd += f" -B {Settings['paths']['build cache']}"
        cmd += f" -DPROJECT_NAME={Settings['ProjectName']}"
        cmd += f" -DPROJECT_BASE_SCRIPT_PATH={Settings['paths']['scripts']}"
        cmd += f" && cmake --build {Settings['paths']['build cache']}"
        cmd += " -- -j $(nproc) -k"
        return cmd

    def get_repo_build_filename(self) -> str:
        return "CMakeLists.txt"

    def get_repo_template(self) -> str:
        return "repository/CMakeLists.txt"

    def get_spec_filename(self) -> "str | None":
        return CMAKE_SPEC_FILENAME

    def make_repo_include_entry(self, repo_source: str, build_path: str,
                                is_independent: bool) -> str:
        if is_independent:
            return f'add_subdirectory("{repo_source}" "{build_path}")'
        return 'include("' + JoinPaths(build_path, "CMakeLists.txt") + '")'
